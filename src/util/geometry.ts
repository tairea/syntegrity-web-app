/**
 * Syntegrity geometries — the precomputed strut tables per shape.
 *
 * WHY THESE ARE HARD-CODED, NOT COMPUTED AT RUNTIME
 * The member structure of a polyhedron (which vertices an edge touches) is
 * trivial, but the *critic* structure (which 2 non-incident teams each strut
 * critiques such that every team receives exactly `teamSize` critics) is a
 * balanced combinatorial design. Beer/Truss solved these by hand per solid.
 * We treat them as fixed, validated lookup tables. The only runtime computation
 * is matching PEOPLE to struts (the role-assignment scripts).
 *
 * STATUS: the tetrahedron is fully encoded and hand-verifiable, so the scripts
 * and scheduler have a real, testable shape today. The larger solids are
 * registered with their metadata but their strut/face tables are pending
 * (TODO: encode + unit-test member/critic balance for each). Keeping each shape
 * behind `getShape()` means scripts never hard-depend on a half-filled table.
 */

