// Public helpers
// ─────────────────────────────────────────────────────────────────────────────

export class ShapeNotEncodedError extends Error {
  readonly shape: ShapeName;
  constructor(shape: ShapeName) {
    super(`Strut table for "${shape}" is not encoded yet (only metadata is known).`);
    this.name = 'ShapeNotEncodedError';
    this.shape = shape;
  }
}

/** Get a fully-encoded shape, or throw if its table is still pending. */
export function getShape(name: ShapeName): SyntegrityShape {
  const shape = ENCODED_SHAPES[name];
  if (!shape) throw new ShapeNotEncodedError(name);
  return shape;
}

export function isShapeEncoded(name: ShapeName): boolean {
  return ENCODED_SHAPES[name] !== undefined;
}

/**
 * Pick the recommended shape for a headcount, following the spec's rule
 * "fit the group to the shape, not the shape to the group". Returns the shape
 * meta plus how many participants to pad/trim to land exactly on it. At 12 the
 * caller is told octahedron is the default but cube is an alternative.
 */
export interface ShapeRecommendation {
  recommended: ShapeMeta;
  /** + => recruit this many more; - => trim this many; 0 => exact. */
  delta: number;
  alternative?: ShapeMeta;
  note?: string;
}

export function recommendShape(headcount: number): ShapeRecommendation {
  const order: ShapeName[] = ['tetrahedron', 'octahedron', 'cuboctahedron', 'icosahedron', 'icosidodecahedron'];

  if (headcount <= 6) {
    return { recommended: SHAPE_META.tetrahedron, delta: 6 - headcount };
  }
  if (headcount === 12) {
    return {
      recommended: SHAPE_META.octahedron,
      delta: 0,
      alternative: SHAPE_META.cube,
      note: 'Octahedron = 6 topics / teams of 4. Cube = 8 topics / teams of 3.',
    };
  }

  // Snap to the nearest exact participant count among the primary forms.
  let best = SHAPE_META[order[0]];
  let bestDist = Math.abs(headcount - best.participantCount);
  for (const name of order) {
    const m = SHAPE_META[name];
    const d = Math.abs(headcount - m.participantCount);
    if (d < bestDist) {
      best = m;
      bestDist = d;
    }
  }
  return { recommended: best, delta: best.participantCount - headcount };
}

// ─────────────────────────────────────────────────────────────────────────────
// Validation — run on any candidate table to confirm it satisfies the invariants.
// Used by unit tests when new shape tables are encoded.
// ─────────────────────────────────────────────────────────────────────────────

export interface ShapeValidation {
  ok: boolean;
  errors: string[];
}

export function validateShape(shape: SyntegrityShape): ShapeValidation {
  const errors: string[] = [];
  const { struts, participantCount, topicCount, teamSize } = shape;

  if (struts.length !== participantCount) {
    errors.push(`struts.length (${struts.length}) !== participantCount (${participantCount})`);
  }

  const memberCount = new Array(topicCount).fill(0);
  const criticCount = new Array(topicCount).fill(0);

  for (const s of struts) {
    const member = new Set(s.memberOf);
    const critic = new Set(s.criticOf);
    if (member.size !== 2) errors.push(`strut ${s.id}: memberOf must be 2 distinct vertices`);
    if (critic.size !== 2) errors.push(`strut ${s.id}: criticOf must be 2 distinct vertices`);
    for (const v of s.criticOf) {
      if (member.has(v)) errors.push(`strut ${s.id}: critiques a team it is a member of (vertex ${v})`);
    }
    for (const v of s.memberOf) memberCount[v]++;
    for (const v of s.criticOf) criticCount[v]++;
  }

  for (let v = 0; v < topicCount; v++) {
    if (memberCount[v] !== teamSize) errors.push(`vertex ${v}: ${memberCount[v]} members, expected ${teamSize}`);
    if (criticCount[v] !== teamSize) errors.push(`vertex ${v}: ${criticCount[v]} critics, expected ${teamSize}`);
  }

  return { ok: errors.length === 0, errors };
}
